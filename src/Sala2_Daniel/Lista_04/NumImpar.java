public class NumImpar {

    //Faça um programa que imprima na tela apenas os números ímpares entre 1 e  50. 

    public static void main(String[] args) {
        for(int i = 1; i < 50; i+=2){
            System.out.println("Seu número atual é: "+i);
        }
    }
}
